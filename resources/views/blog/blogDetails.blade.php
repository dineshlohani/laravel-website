                            <h3 class="title">Switching the Student Visa from the Language School</h3>
                            <p class="disc para-1">
                            What happens if you finally arrive in Japan to enroll in a 
                            language school and then decide on the spur of the moment 
                            that you want to continue your education at a different institution? 
                            You would undoubtedly worry about your visa in addition to the money, 
                            lodging, and other things. There you are. Also, I’ve been there! I also 
                            took all the necessary steps to fulfill my newly discovered desire of studying in Japan. This post
                             will compile all of the information you need to be ready to switch from your current 
                             language school visa to another student visa.
                            </p>
                            <h4><u>How to Switch from a Language School Visa to a Different Student Visa for Japan Study</u></h4>
                            <p class="disc">
                            A Japanese student visa comes in many grades. What you apply for will depend on 
                            how long you want to study for and where you will be located. The limits on each
                             student visa vary as well. The most likely benefits are from attending a Japanese
                             university. Since your original intention was just to enroll in a language program, 
                             you have undoubtedly already identified the kind of visa you need. However, now that
                              you’ve made the decision to continue your studies in Japan, it’s time to focus on getting
                               a visa! Just relax. Obtaining paperwork in Japan as a foreigner caused me some anxiety as well.
                                Please be aware that your school is always there to assist you, and they can even submit your 
                                application! Even if my school was helping me, in my instance, I still needed to know the procedure.
                                 I was given a list of the required paperwork after asking what I should do. I then submitted the petitions 
                            to both my old and new schools, and I finally waited for my modified visa. Let’s go to work on your to-do list now:
                            </p>
