                            <h5>Existing school</h5>
                            <p class="disc">
                            You may seek for help with your student visa no matter where you are enrolled right now. 
                            You will often need to get your most recent transcript of grades as well as your attendance record from school.
                             These papers will demonstrate your strong academic performance! In order for your application to be approved,
                              keep in mind that your presence is important. In addition to these two papers, I had to provide a certificate 
                              of anticipated graduation. Rest confident that your present school will let you know if your language school 
                              needs anything more in addition to the aforementioned documentation. They have a responsibility to provide you 
                              anything you could need for altering your visa.
                             You may then go to your new school to finish up a different set of prerequisites.
